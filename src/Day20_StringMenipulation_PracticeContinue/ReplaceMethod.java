package Day20_StringMenipulation_PracticeContinue;

public class ReplaceMethod {
    public static void main(String[] args) {
        String word = "Aly";

        System.out.println(word.replace("Aly", "Ali"));
        System.out.println(word.replace("y", "i"));
        System.out.println(word.replace("Aly", "Ali Nijat"));

        System.out.println(word.replace("Aly", " "));
    }
}

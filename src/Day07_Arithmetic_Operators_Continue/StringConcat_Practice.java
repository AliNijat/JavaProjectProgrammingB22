package Day07_Arithmetic_Operators_Continue;

public class StringConcat_Practice {
    public static void main(String[] args) {

        String str1, str2;
        str1 = "Hello";
        str2 = "Friends";

        System.out.println(str1 + str2);
        System.out.println(str1 + " " + str2);
        System.out.println("------------------------");

        int num1 = 7;
        int num2 = 8;

        System.out.println(num1 + num2);
        System.out.println(num1 + " " + num2);
        System.out.println(num1 + "" + num2);
        System.out.println("------------------------");

        char ch1 = 'a';
        char ch2 = 'b';

        System.out.println(ch1 + ch2);
        System.out.println(ch1 +" "+ch2);
        System.out.println(ch1+ ""+ch2);
        System.out.println("-----------------------");

        int ch3 = 'a';
        int ch4 = 'b';
        System.out.println(ch3 + ch4);
        System.out.println(ch3 + " " +ch4);
        System.out.println(ch3 + "" +ch4);
        System.out.println("-------------------------");



    }
}

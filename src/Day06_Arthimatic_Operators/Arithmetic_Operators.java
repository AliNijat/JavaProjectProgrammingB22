package Day06_Arthimatic_Operators;

public class Arithmetic_Operators {
    public static void main(String[] args) {

        // We will work on [ + , - , * , / and % ]
        System.out.println(5 + 10); // = 15
        System.out.println( 10 - 5); // = 5
        System.out.println(5 * 10); // = 50
        System.out.println(10 / 5); // = 2
        System.out.println(10 % 5); // = 0

        // Remainder
        System.out.println( 13 % 5); // = 3

        System.out.println("________________________________");

        // Now lets do negatives numbers/values
        System.out.println(5 - 10); // = -5
        System.out.println(5 / 10 ); // = 0
        System.out.println(0.5 / 10 ); // == 0.05
        System.out.println(0.5 / 0.10); // = 5.0
        System.out.println(5 % 10); // = 0


    }
}

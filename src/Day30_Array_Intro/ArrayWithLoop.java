package Day30_Array_Intro;

public class ArrayWithLoop {
    public static void main(String[] args) {

        int nums [] = {12, 34, 45, 667, 676, 100,  20};

        for (int i = 0; i < nums.length ; i++) {
            System.out.print(nums[i] + " ");

        }
        System.out.println();
        for (int i = 0; i < 7; i++) {
            System.out.print(nums [i] + " ");

        }
        System.out.println();



    }
}

package Day03_Escape_Sequence;

public class Escape_Practice {
    public static void main(String[] args){

        System.out.println("\""); // = "
        System.out.println("I like Java \n programming"); // new line
        System.out.println("you are good \t boy"); // making space
        System.out.println("How are you doing \b now");
        System.out.println("'' and '");
        System.out.println("Wooow, \r it's work nice");
        System.out.println("Good practice \f in java ");
        System.out.println("I love \"Java\" Programming!");
        System.out.println("I need to buy \"Coffee\" and \"Tea\" now");


    }
}
